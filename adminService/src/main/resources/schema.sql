DROP TABLE IF EXISTS AMCUSTOMERROLE_SERVICE_MAP;
DROP TABLE IF EXISTS AMCUSTOMERROLE;
DROP TABLE IF EXISTS AMCUSTOMERSERVICE;
DROP TABLE IF EXISTS AMSERVICECATEGORY;
DROP TABLE IF EXISTS AMROLE;
DROP TABLE IF EXISTS AMCUSTOMERMAIN;

CREATE TABLE AMCUSTOMERMAIN (
    CUSTOMER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FIRST_NAME VARCHAR(80) NOT NULL,
    LAST_NAME VARCHAR(80) NOT NULL,
    EMAIL VARCHAR(160) NOT NULL UNIQUE,
    PHONE VARCHAR(30) NOT NULL,
    COMPANY_NAME VARCHAR(160) NOT NULL,
    COMPANY_TYPE VARCHAR(60) NOT NULL,
    GST_NUMBER VARCHAR(30),
    COMPANY_SIZE VARCHAR(40) NOT NULL,
    INDUSTRY VARCHAR(80) NOT NULL,
    ADDRESS_LINE1 VARCHAR(200) NOT NULL,
    ADDRESS_LINE2 VARCHAR(200),
    CITY VARCHAR(80) NOT NULL,
    STATE VARCHAR(80) NOT NULL,
    PINCODE VARCHAR(20) NOT NULL,
    COUNTRY VARCHAR(80) NOT NULL,
    PLAN_NAME VARCHAR(80) NOT NULL,
    BILLING_CYCLE VARCHAR(40) NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    ONBOARDED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE AMROLE (
    ROLE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE_NAME VARCHAR(120) NOT NULL,
    ROLE_CODE VARCHAR(80) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR(400),
    PERMISSIONS VARCHAR(400) NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE AMSERVICECATEGORY (
    SERVICE_CATEGORY_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CATEGORY_NUMBER INTEGER NOT NULL UNIQUE,
    CATEGORY_NAME VARCHAR(120) NOT NULL,
    DESCRIPTION VARCHAR(400),
    STATUS VARCHAR(20) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE AMCUSTOMERSERVICE (
    CUSTOMER_SERVICE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SERVICE_NUMBER INTEGER NOT NULL UNIQUE,
    SERVICE_NAME VARCHAR(120) NOT NULL,
    SERVICE_CATEGORY_ID BIGINT NOT NULL,
    DESCRIPTION VARCHAR(400),
    STATUS VARCHAR(20) NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_AMCUSTOMERSERVICE_CATEGORY FOREIGN KEY (SERVICE_CATEGORY_ID)
        REFERENCES AMSERVICECATEGORY (SERVICE_CATEGORY_ID)
);

CREATE TABLE AMCUSTOMERROLE (
    CUSTOMER_ROLE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID BIGINT NOT NULL,
    ROLE_ID BIGINT NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    ASSIGNED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_AMCUSTOMERROLE_CUSTOMER FOREIGN KEY (CUSTOMER_ID)
        REFERENCES AMCUSTOMERMAIN (CUSTOMER_ID),
    CONSTRAINT FK_AMCUSTOMERROLE_ROLE FOREIGN KEY (ROLE_ID)
        REFERENCES AMROLE (ROLE_ID)
);

CREATE TABLE AMCUSTOMERROLE_SERVICE_MAP (
    CUSTOMER_ROLE_ID BIGINT NOT NULL,
    CUSTOMER_SERVICE_ID BIGINT NOT NULL,
    PRIMARY KEY (CUSTOMER_ROLE_ID, CUSTOMER_SERVICE_ID),
    CONSTRAINT FK_AMCUSTOMERROLE_SERVICE_ROLE FOREIGN KEY (CUSTOMER_ROLE_ID)
        REFERENCES AMCUSTOMERROLE (CUSTOMER_ROLE_ID) ON DELETE CASCADE,
    CONSTRAINT FK_AMCUSTOMERROLE_SERVICE_SERVICE FOREIGN KEY (CUSTOMER_SERVICE_ID)
        REFERENCES AMCUSTOMERSERVICE (CUSTOMER_SERVICE_ID)
);
